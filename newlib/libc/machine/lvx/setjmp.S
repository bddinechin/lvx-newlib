#include "asm.h"
#include "jmpbuf.h"

/*
 * Saved layout is as follows (see jmpbuf.h, generated by lvx-mds's
 * BE/LIBC backend from Convention-lvx-regular):
 * LVX_JMPBUF_R18_OFFSET: $r18 $r19
 * LVX_JMPBUF_R20_OFFSET: $r20 $r21 $r22 $r23
 * LVX_JMPBUF_R24_OFFSET: $r24 $r25 $r26 $r27
 * LVX_JMPBUF_R28_OFFSET: $r28 $r29 $r30 $r31
 * LVX_JMPBUF_R13_OFFSET: $r13 $r14 $ls  $le
 * LVX_JMPBUF_LC_OFFSET:  $lc  $cs  $ra  $r12
 *
 * gdb/gdb/lvx-common-tdep.c's get_longjmp_target() reads the same
 * LVX_JMPBUF_RA_OFFSET from its own copy of this generated header.
 */

    ## int setjmp(jmp_buf)
    ##
    ## Basically setjmp saves the set of callee saved registers in jmp_buf
    ## Also saving cs, the loop registers, ra and sp
_ENTRY(setjmp)
    .file 1 "setjmp.S"
    .loc 1 54 0
    ## All preserved registers
    sq LVX_JMPBUF_R18_OFFSET[$r0] = $r18r19
    copyd   $r32 = $r13
    get     $r34 = $ls
;;
    so LVX_JMPBUF_R20_OFFSET[$r0] = $r20r21r22r23
    copyd   $r33 = $r14
    get     $r35 = $le
;;
    so LVX_JMPBUF_R24_OFFSET[$r0] = $r24r25r26r27
    get     $r36 = $lc
;;
    so LVX_JMPBUF_R28_OFFSET[$r0] = $r28r29r30r31
    get     $r37 = $cs
;;
    so LVX_JMPBUF_R13_OFFSET[$r0] = $r32r33r34r35
    get     $r38 = $ra
    copyd   $r39 = $r12
;;
    so LVX_JMPBUF_LC_OFFSET[$r0] = $r36r37r38r39
    ## Must return zero if called to set up jmp_buf
    make     $r0 = 0
    ret
;;
    .endp setjmp



    ## void longjmp(jmp_buf env, int val)
    ##
    ## Basically restore the saved register set found in env to processor
    ## Restoring also sp. Note that it jumps back to original location
_ENTRY(longjmp)
        .loc 1 88 0
    lo $r32r33r34r35 = LVX_JMPBUF_R13_OFFSET[$r0]
;;
    lo $r36r37r38r39 = LVX_JMPBUF_LC_OFFSET[$r0]
    copyd   $r13 = $r32
    copyd   $r14 = $r33
    set     $ls = $r34
;;
    lq $r18r19 = LVX_JMPBUF_R18_OFFSET[$r0]
    set     $le = $r35
;;
    lo $r20r21r22r23 = LVX_JMPBUF_R20_OFFSET[$r0]
    set     $lc = $r36
;;
    lo $r24r25r26r27 = LVX_JMPBUF_R24_OFFSET[$r0]
    set     $cs = $r37
;;
    lo $r28r29r30r31 = LVX_JMPBUF_R28_OFFSET[$r0]
    set     $ra = $r38
    copyd   $r12 = $r39
;;
    ## Finally if called with val==0, return one else val
    maxud   $r0 = $r1, 1
    ret
;;
    .endp longjmp
